#include <iostream>
using namespace std;

int main()
{
	const int JumtDt_ernan=3;
	int Nilai [JumtDt_ernan] = {22, 7,1966};
	int i, Total=0;
	cout << endl;
	cout << "Menginput ARRAY/LARIK 1 Dimensi " << endl;
	cout << "~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~" << endl;
	for (i=0; i < JumtDt_ernan; i++)
	{
		cout << " Isi Elemen Ke-" << i+1 <<" : " << Nilai [i]<< endl;
		Total = Total + Nilai [i];
		
	}
	cout << endl;
	cout << "~~~~~~~~~~~~~~~~~~~~~~~~~~~~" << endl;
	cout << "Total dari Penjumlahan = " << Total << endl;
	cin.get();
}
